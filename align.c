#include "printflib.h"

int align(char c)
{
	if (c == '-')
		return (1);
	if (c == '+')
		return (2);
	if (c == ' ')
		return (3);
	if (c == '#')
		return (4);
	if (c == '0')
		return (5);
	return (0);
}